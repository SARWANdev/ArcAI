from enum import Enum

class Color(Enum):
    """
    Enum class representing various colors with their corresponding hexadecimal values.
    Each color is represented by a name and a hex code.
    """
    BLACK = "#3F3B3B"
    GRAY = "#B0ABAA"
    PINK = "#E097BF"
    PURPLE = "#A38DD6"
    BLUE = "#69BCD8"
    GREEN = "#51B27C"
    YELLOW = "#FBE675"
    ORANGE = "#ED974F"
    RED = "#DB5E52"
